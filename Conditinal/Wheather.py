weather=input("Enter the weather condition (sunny, rainy, snowy): ")
if weather=="sunny":
    print("It's a sunny day. Wear sunglasses and light clothing.")
elif weather=="rainy":
    print("It's raining. Don't forget to carry an umbrella and wear waterproof shoes.")    
elif weather=="snowy":
    print("It's snowing. Wear warm clothes and boots.")    